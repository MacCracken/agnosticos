// ---------------------------------------------------------------------------
// JobStatus
// ---------------------------------------------------------------------------

/// Lifecycle state of a fine-tuning job.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub enum JobStatus {
    Queued,
    Preparing,
    Training,
    Evaluating,
    Complete,
    Failed(String),
    Cancelled,
}

impl JobStatus {
    /// Whether a transition from `self` to `to` is allowed.
    pub fn valid_transition(&self, to: &JobStatus) -> bool {
        matches!(
            (self, to),
            (Self::Queued, Self::Preparing)
                | (Self::Queued, Self::Cancelled)
                | (Self::Queued, Self::Failed(_))
                | (Self::Preparing, Self::Training)
                | (Self::Preparing, Self::Failed(_))
                | (Self::Preparing, Self::Cancelled)
                | (Self::Training, Self::Evaluating)
                | (Self::Training, Self::Failed(_))
                | (Self::Training, Self::Cancelled)
                | (Self::Evaluating, Self::Complete)
                | (Self::Evaluating, Self::Failed(_))
                | (Self::Evaluating, Self::Cancelled)
        )
    }

    /// Whether the job is in a terminal state (no further transitions).
    pub fn is_terminal(&self) -> bool {
        matches!(self, Self::Complete | Self::Failed(_) | Self::Cancelled)
    }
}

impl std::fmt::Display for JobStatus {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::Queued => write!(f, "Queued"),
            Self::Preparing => write!(f, "Preparing"),
            Self::Training => write!(f, "Training"),
            Self::Evaluating => write!(f, "Evaluating"),
            Self::Complete => write!(f, "Complete"),
            Self::Failed(msg) => write!(f, "Failed({})", msg),
            Self::Cancelled => write!(f, "Cancelled"),
        }
    }
}

