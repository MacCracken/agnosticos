// ---------------------------------------------------------------------------
// ModelRegistry
// ---------------------------------------------------------------------------

/// Registry tracking all fine-tuned models.
#[derive(Debug, Default)]
pub struct ModelRegistry {
    models: Vec<FineTunedModel>,
}

impl ModelRegistry {
    pub fn new() -> Self {
        Self {
            models: Vec::new(),
        }
    }

    pub fn register_model(&mut self, model: FineTunedModel) -> Result<()> {
        if self.models.iter().any(|m| m.model_id == model.model_id) {
            bail!("model with id '{}' already exists", model.model_id);
        }
        info!(model_id = %model.model_id, name = %model.name, "registering fine-tuned model");
        self.models.push(model);
        Ok(())
    }

    pub fn get_model(&self, model_id: &str) -> Option<&FineTunedModel> {
        self.models.iter().find(|m| m.model_id == model_id)
    }

    pub fn models_for_agent(&self, agent_id: &str) -> Vec<&FineTunedModel> {
        self.models.iter().filter(|m| m.agent_id == agent_id).collect()
    }

    pub fn list_models(&self) -> &[FineTunedModel] {
        &self.models
    }

    /// Return the model with the lowest `final_loss` for the given agent.
    pub fn best_model_for_agent(&self, agent_id: &str) -> Option<&FineTunedModel> {
        self.models
            .iter()
            .filter(|m| m.agent_id == agent_id)
            .min_by(|a, b| {
                a.metrics
                    .final_loss
                    .partial_cmp(&b.metrics.final_loss)
                    .unwrap_or(std::cmp::Ordering::Equal)
            })
    }
}

