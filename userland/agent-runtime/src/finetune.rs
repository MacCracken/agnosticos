/// Minimum number of training examples required to submit a job.
const MIN_EXAMPLES: usize = 10;

/// Orchestrates the fine-tuning lifecycle: datasets, jobs, and model output.
pub struct FineTunePipeline {
    datasets: Vec<TrainingDataset>,
    jobs: Vec<FineTuneJob>,
    pub model_registry: ModelRegistry,
}

impl FineTunePipeline {
    pub fn new() -> Self {
        info!("initialising fine-tune pipeline");
        Self {
            datasets: Vec::new(),
            jobs: Vec::new(),
            model_registry: ModelRegistry::new(),
        }
    }

    // -- Dataset operations -------------------------------------------------

    /// Create a new empty dataset, returning its ID.
    pub fn create_dataset(
        &mut self,
        name: impl Into<String>,
        agent_id: impl Into<String>,
    ) -> Result<String> {
        let ds = TrainingDataset::new(name, agent_id);
        let id = ds.dataset_id.clone();
        info!(dataset_id = %id, "created training dataset");
        self.datasets.push(ds);
        Ok(id)
    }

    pub fn get_dataset(&self, dataset_id: &str) -> Option<&TrainingDataset> {
        self.datasets.iter().find(|d| d.dataset_id == dataset_id)
    }

    pub fn get_dataset_mut(&mut self, dataset_id: &str) -> Option<&mut TrainingDataset> {
        self.datasets.iter_mut().find(|d| d.dataset_id == dataset_id)
    }

    /// Add examples to a dataset; returns the count actually added.
    pub fn add_examples(
        &mut self,
        dataset_id: &str,
        examples: Vec<TrainingExample>,
    ) -> Result<usize> {
        let ds = self
            .get_dataset_mut(dataset_id)
            .ok_or_else(|| anyhow::anyhow!("dataset not found: {}", dataset_id))?;
        let count = examples.len();
        for ex in examples {
            ds.add_example(ex);
        }
        debug!(dataset_id = %dataset_id, count = count, "added examples to dataset");
        Ok(count)
    }

    // -- Job operations -----------------------------------------------------

    /// Validate config and enqueue a new fine-tuning job.
    pub fn submit_job(&mut self, config: FineTuneConfig) -> Result<String> {
        // Validate learning rate
        if config.learning_rate <= 0.0 || config.learning_rate > 1.0 {
            bail!(
                "learning rate must be in (0.0, 1.0], got {}",
                config.learning_rate
            );
        }

        // Validate epochs
        if config.epochs == 0 || config.epochs > 100 {
            bail!("epochs must be in [1, 100], got {}", config.epochs);
        }

        // Validate batch_size
        if config.batch_size == 0 {
            bail!("batch_size must be > 0, got {}", config.batch_size);
        }

        // Validate max_sequence_length
        if config.max_sequence_length == 0 {
            bail!(
                "max_sequence_length must be > 0, got {}",
                config.max_sequence_length
            );
        }

        // Validate dataset exists and has enough examples
        let ds = self
            .get_dataset(&config.dataset_id)
            .ok_or_else(|| anyhow::anyhow!("dataset not found: {}", config.dataset_id))?;

        if ds.examples.len() < MIN_EXAMPLES {
            bail!(
                "dataset has {} examples, minimum {} required",
                ds.examples.len(),
                MIN_EXAMPLES
            );
        }

        let job = FineTuneJob::new(config);
        let job_id = job.job_id.clone();
        info!(job_id = %job_id, "submitted fine-tune job");
        self.jobs.push(job);
        Ok(job_id)
    }

    pub fn get_job(&self, job_id: &str) -> Option<&FineTuneJob> {
        self.jobs.iter().find(|j| j.job_id == job_id)
    }

    fn get_job_mut(&mut self, job_id: &str) -> Option<&mut FineTuneJob> {
        self.jobs.iter_mut().find(|j| j.job_id == job_id)
    }

    /// Update progress on a running job.
    pub fn update_progress(&mut self, job_id: &str, progress: JobProgress) -> Result<()> {
        let job = self
            .get_job_mut(job_id)
            .ok_or_else(|| anyhow::anyhow!("job not found: {}", job_id))?;

        if job.status.is_terminal() {
            bail!("cannot update progress on terminal job {}", job_id);
        }

        // Auto-transition Queued -> Preparing -> Training on first progress update
        if job.status == JobStatus::Queued {
            job.transition(JobStatus::Preparing)?;
            job.transition(JobStatus::Training)?;
            job.started_at = Some(Utc::now());
        } else if job.status == JobStatus::Preparing {
            job.transition(JobStatus::Training)?;
            job.started_at = Some(Utc::now());
        }

        job.progress = progress;
        debug!(job_id = %job_id, pct = %job.progress.percentage(), "updated job progress");
        Ok(())
    }

    /// Mark a job as successfully completed and record the model path.
    pub fn complete_job(&mut self, job_id: &str, model_path: String) -> Result<()> {
        let job = self
            .get_job_mut(job_id)
            .ok_or_else(|| anyhow::anyhow!("job not found: {}", job_id))?;

        // Transition through Evaluating to Complete if needed
        if job.status == JobStatus::Training {
            job.transition(JobStatus::Evaluating)?;
        }
        job.transition(JobStatus::Complete)?;
        job.completed_at = Some(Utc::now());
        job.output_model_path = Some(model_path);
        info!(job_id = %job_id, "fine-tune job completed");
        Ok(())
    }

    /// Mark a job as failed with an error message.
    pub fn fail_job(&mut self, job_id: &str, error: String) -> Result<()> {
        let job = self
            .get_job_mut(job_id)
            .ok_or_else(|| anyhow::anyhow!("job not found: {}", job_id))?;

        if job.status.is_terminal() {
            bail!("job {} is already in terminal state {}", job_id, job.status);
        }

        job.transition(JobStatus::Failed(error.clone()))?;
        job.error = Some(error);
        job.completed_at = Some(Utc::now());
        warn!(job_id = %job_id, "fine-tune job failed");
        Ok(())
    }

    /// Cancel a non-terminal job.
    pub fn cancel_job(&mut self, job_id: &str) -> Result<()> {
        let job = self
            .get_job_mut(job_id)
            .ok_or_else(|| anyhow::anyhow!("job not found: {}", job_id))?;

        if job.status.is_terminal() {
            bail!("job {} is already in terminal state {}", job_id, job.status);
        }

        job.transition(JobStatus::Cancelled)?;
        job.completed_at = Some(Utc::now());
        info!(job_id = %job_id, "fine-tune job cancelled");
        Ok(())
    }

    /// Return all jobs that are not in a terminal state.
    pub fn active_jobs(&self) -> Vec<&FineTuneJob> {
        self.jobs.iter().filter(|j| !j.status.is_terminal()).collect()
    }

    /// Return all jobs whose dataset belongs to the given agent.
    pub fn jobs_for_agent(&self, agent_id: &str) -> Vec<&FineTuneJob> {
        let agent_dataset_ids: Vec<&str> = self
            .datasets
            .iter()
            .filter(|d| d.agent_id == agent_id)
            .map(|d| d.dataset_id.as_str())
            .collect();

        self.jobs
            .iter()
            .filter(|j| agent_dataset_ids.contains(&j.config.dataset_id.as_str()))
            .collect()
    }

    /// Aggregate pipeline statistics.
    pub fn pipeline_stats(&self) -> PipelineStats {
        let total_examples: usize = self.datasets.iter().map(|d| d.examples.len()).sum();
        let completed: Vec<&FineTuneJob> = self
            .jobs
            .iter()
            .filter(|j| j.status == JobStatus::Complete)
            .collect();
        let failed = self
            .jobs
            .iter()
            .filter(|j| matches!(j.status, JobStatus::Failed(_)))
            .count();
        let active = self.active_jobs().len();

        let avg_time = if completed.is_empty() {
            None
        } else {
            let total_secs: f64 = completed
                .iter()
                .filter_map(|j| {
                    let start = j.started_at?;
                    let end = j.completed_at?;
                    Some((end - start).num_seconds() as f64)
                })
                .sum();
            let count = completed
                .iter()
                .filter(|j| j.started_at.is_some() && j.completed_at.is_some())
                .count();
            if count > 0 {
                Some(total_secs / count as f64)
            } else {
                Some(0.0)
            }
        };

        PipelineStats {
            total_datasets: self.datasets.len(),
            total_examples,
            total_jobs: self.jobs.len(),
            completed_jobs: completed.len(),
            failed_jobs: failed,
            active_jobs: active,
            average_training_time_secs: avg_time,
        }
    }
}

