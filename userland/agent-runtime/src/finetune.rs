/// Aggregate statistics for a training dataset.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct DatasetStats {
    pub total_examples: usize,
    pub avg_input_length: f64,
    pub avg_output_length: f64,
    /// Bucketed quality distribution: "0.0-0.2", "0.2-0.4", etc.
    pub quality_distribution: HashMap<String, usize>,
    /// Count of examples per source type.
    pub source_distribution: HashMap<ExampleSource, usize>,
}

impl Default for DatasetStats {
    fn default() -> Self {
        Self {
            total_examples: 0,
            avg_input_length: 0.0,
            avg_output_length: 0.0,
            quality_distribution: HashMap::new(),
            source_distribution: HashMap::new(),
        }
    }
}

// ---------------------------------------------------------------------------
// TrainingDataset
// ---------------------------------------------------------------------------

/// A named collection of training examples for a specific agent.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct TrainingDataset {
    pub dataset_id: String,
    pub name: String,
    pub agent_id: String,
    pub examples: Vec<TrainingExample>,
    pub created_at: DateTime<Utc>,
    pub updated_at: DateTime<Utc>,
    pub stats: DatasetStats,
}

impl TrainingDataset {
    pub fn new(name: impl Into<String>, agent_id: impl Into<String>) -> Self {
        let now = Utc::now();
        Self {
            dataset_id: Uuid::new_v4().to_string(),
            name: name.into(),
            agent_id: agent_id.into(),
            examples: Vec::new(),
            created_at: now,
            updated_at: now,
            stats: DatasetStats::default(),
        }
    }

    /// Append an example and refresh stats.
    pub fn add_example(&mut self, example: TrainingExample) {
        self.examples.push(example);
        self.updated_at = Utc::now();
        self.stats = self.compute_stats();
    }

    /// Remove an example by ID. Returns `true` if found.
    pub fn remove_example(&mut self, example_id: &str) -> bool {
        let before = self.examples.len();
        self.examples.retain(|e| e.example_id != example_id);
        let removed = self.examples.len() < before;
        if removed {
            self.updated_at = Utc::now();
            self.stats = self.compute_stats();
        }
        removed
    }

    /// Return references to examples whose quality score meets the threshold.
    pub fn filter_by_quality(&self, min_score: f64) -> Vec<&TrainingExample> {
        self.examples
            .iter()
            .filter(|e| e.quality_score.unwrap_or(0.0) >= min_score)
            .collect()
    }

    /// Recompute dataset statistics from the current example set.
    pub fn compute_stats(&self) -> DatasetStats {
        let total = self.examples.len();
        if total == 0 {
            return DatasetStats::default();
        }

        let avg_input = self.examples.iter().map(|e| e.input.len()).sum::<usize>() as f64
            / total as f64;
        let avg_output = self.examples.iter().map(|e| e.output.len()).sum::<usize>() as f64
            / total as f64;

        let mut quality_dist: HashMap<String, usize> = HashMap::new();
        for e in &self.examples {
            let score = e.quality_score.unwrap_or(0.0);
            let bucket = if score < 0.2 {
                "0.0-0.2"
            } else if score < 0.4 {
                "0.2-0.4"
            } else if score < 0.6 {
                "0.4-0.6"
            } else if score < 0.8 {
                "0.6-0.8"
            } else {
                "0.8-1.0"
            };
            *quality_dist.entry(bucket.to_string()).or_insert(0) += 1;
        }

        let mut source_dist: HashMap<ExampleSource, usize> = HashMap::new();
        for e in &self.examples {
            *source_dist.entry(e.source).or_insert(0) += 1;
        }

        DatasetStats {
            total_examples: total,
            avg_input_length: avg_input,
            avg_output_length: avg_output,
            quality_distribution: quality_dist,
            source_distribution: source_dist,
        }
    }
}

// ---------------------------------------------------------------------------
// FineTuneMethod
// ---------------------------------------------------------------------------

/// Approach used for fine-tuning a model.
#[derive(Debug, Clone, PartialEq, Serialize, Deserialize)]
pub enum FineTuneMethod {
    /// Full parameter update — most expensive, highest quality.
    FullFineTune,
    /// Low-Rank Adaptation — efficient parameter-efficient fine-tuning.
    LoRA { rank: u32, alpha: f64 },
    /// Quantised LoRA — even lower VRAM via quantisation.
    QLoRA { rank: u32, alpha: f64, bits: u8 },
    /// Prefix-tuning — prepend learnable tokens to the input.
    Prefix { prefix_length: u32 },
}

impl FineTuneMethod {
    /// Estimated VRAM multiplier relative to base model size.
    pub fn vram_multiplier(&self) -> f64 {
        match self {
            Self::FullFineTune => 4.0,
            Self::LoRA { .. } => 1.2,
            Self::QLoRA { bits, .. } => {
                if *bits <= 4 {
                    0.5
                } else {
                    0.7
                }
            }
            Self::Prefix { .. } => 1.1,
        }
    }
}

impl std::fmt::Display for FineTuneMethod {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::FullFineTune => write!(f, "FullFineTune"),
            Self::LoRA { rank, alpha } => write!(f, "LoRA(rank={}, alpha={})", rank, alpha),
            Self::QLoRA { rank, alpha, bits } => {
                write!(f, "QLoRA(rank={}, alpha={}, {}bit)", rank, alpha, bits)
            }
            Self::Prefix { prefix_length } => write!(f, "Prefix(len={})", prefix_length),
        }
    }
}

