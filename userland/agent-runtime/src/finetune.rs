/// A single fine-tuning job tracked by the pipeline.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct FineTuneJob {
    pub job_id: String,
    pub config: FineTuneConfig,
    pub status: JobStatus,
    pub created_at: DateTime<Utc>,
    pub started_at: Option<DateTime<Utc>>,
    pub completed_at: Option<DateTime<Utc>>,
    pub progress: JobProgress,
    pub error: Option<String>,
    pub output_model_path: Option<String>,
}

impl FineTuneJob {
    fn new(config: FineTuneConfig) -> Self {
        Self {
            job_id: Uuid::new_v4().to_string(),
            config,
            status: JobStatus::Queued,
            created_at: Utc::now(),
            started_at: None,
            completed_at: None,
            progress: JobProgress::default(),
            error: None,
            output_model_path: None,
        }
    }

    /// Attempt a status transition; returns an error on invalid transitions.
    fn transition(&mut self, to: JobStatus) -> Result<()> {
        if !self.status.valid_transition(&to) {
            bail!(
                "invalid job status transition: {} -> {}",
                self.status,
                to
            );
        }
        debug!(job_id = %self.job_id, from = %self.status, to = %to, "job status transition");
        self.status = to;
        Ok(())
    }
}

// ---------------------------------------------------------------------------
// VramEstimate
// ---------------------------------------------------------------------------

/// Estimated VRAM breakdown for a fine-tuning job.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct VramEstimate {
    pub model_vram_gb: f64,
    pub optimizer_vram_gb: f64,
    pub activation_vram_gb: f64,
    pub total_gb: f64,
}

/// Estimate VRAM requirements for fine-tuning a model.
///
/// `model_params_millions` is the number of model parameters in millions
/// (e.g. 7000 for a 7B model). Each parameter is assumed to be 2 bytes (fp16).
pub fn estimate_vram(config: &FineTuneConfig, model_params_millions: u64) -> VramEstimate {
    // Base model size in GB (fp16 = 2 bytes per param)
    let base_gb = (model_params_millions as f64 * 1_000_000.0 * 2.0) / 1_073_741_824.0;

    let (model_vram_gb, optimizer_vram_gb, activation_vram_gb) = match &config.method {
        FineTuneMethod::FullFineTune => {
            // Full: model + optimizer states (~2x) + activations (~1x)
            let model = base_gb;
            let optimizer = base_gb * 2.0;
            let activation = base_gb * 1.0;
            (model, optimizer, activation)
        }
        FineTuneMethod::LoRA { .. } => {
            // LoRA: full model loaded + small trainable fraction
            let model = base_gb;
            let optimizer = base_gb * 0.1;
            let activation = base_gb * 0.1;
            (model, optimizer, activation)
        }
        FineTuneMethod::QLoRA { bits, .. } => {
            // QLoRA: quantised model + small trainable fraction
            let quant_factor = if *bits <= 4 { 0.25 } else { 0.5 };
            let model = base_gb * quant_factor;
            let optimizer = base_gb * 0.1;
            let activation = base_gb * 0.1 * quant_factor;
            (model, optimizer, activation)
        }
        FineTuneMethod::Prefix { .. } => {
            // Prefix: full model + small prefix parameters
            let model = base_gb;
            let optimizer = base_gb * 0.05;
            let activation = base_gb * 0.05;
            (model, optimizer, activation)
        }
    };

    let total = model_vram_gb + optimizer_vram_gb + activation_vram_gb;

    VramEstimate {
        model_vram_gb,
        optimizer_vram_gb,
        activation_vram_gb,
        total_gb: total,
    }
}

