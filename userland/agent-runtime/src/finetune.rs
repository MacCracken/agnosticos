//! Fine-Tuning Pipeline for AGNOS Local Model Adaptation
//!
//! Manages the lifecycle of fine-tuning local LLMs for agent-specific tasks:
//! collecting training data, preparing datasets, configuring and tracking
//! fine-tuning jobs, and registering adapted models. The actual GPU training
//! happens externally — this module orchestrates the pipeline.

use std::collections::HashMap;

use anyhow::{bail, Result};
use chrono::{DateTime, Utc};
use serde::{Deserialize, Serialize};
use tracing::{debug, info, warn};
use uuid::Uuid;

// ---------------------------------------------------------------------------
// ExampleSource
// ---------------------------------------------------------------------------

/// Origin of a training example.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum ExampleSource {
    /// Explicit positive/negative feedback from a user.
    UserFeedback,
    /// Generated by the agent interacting with itself.
    AgentSelfPlay,
    /// Hand-curated dataset provided by a developer.
    CuratedDataset,
    /// Extracted from real agent conversation logs.
    ConversationLog,
}

impl std::fmt::Display for ExampleSource {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::UserFeedback => write!(f, "UserFeedback"),
            Self::AgentSelfPlay => write!(f, "AgentSelfPlay"),
            Self::CuratedDataset => write!(f, "CuratedDataset"),
            Self::ConversationLog => write!(f, "ConversationLog"),
        }
    }
}

